public class om {
    public static void main(String[] args) {
        // writing "ॐ नमः शिवाय" using unicode
        String om = "\u0950";
        System.out.print(om);
        System.out.print(' ');

        String nmha = "\u0928\u092E\u0903";
        System.out.print(nmha);
        System.out.print(' ');

        String shv = "\u0936\u093F\u0935\u093E\u092F";
        System.out.print(shv);
        System.out.println();
    }
}
